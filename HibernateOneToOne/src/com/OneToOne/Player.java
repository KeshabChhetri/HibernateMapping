package com.OneToOne;

public class Player {
	private int Pid;
	private String Pname;
	private int Pgoal;
	
	public int getPid() {
		return Pid;
	}
	public void setPid(int pid) {
		Pid = pid;
	}
	public String getPname() {
		return Pname;
	}
	public void setPname(String pname) {
		Pname = pname;
	}
	public int getPgoal() {
		return Pgoal;
	}
	public void setPgoal(int pgoal) {
		Pgoal = pgoal;
	}
	
	
	
}
